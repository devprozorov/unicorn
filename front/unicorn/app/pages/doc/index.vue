// Search functionality
const filteredResults = computed(() => {
  if (!searchQuery.value.trim()) return []
  
  const query = searchQuery.value.toLowerCase()
  const results = []
  
  Object.entries(contentData).forEach(([key, content]) => {
    const titleMatch = content.title.toLowerCase().includes(query)
    const subtitleMatch = content.subtitle.toLowerCase().includes(query)
    
    let contentMatch = false
    if (content.sections) {
      contentMatch = content.sections.some(section => 
        section.title.toLowerCase().includes(query) ||
        section.items.some(item => item.toLowerCase().includes(query))
      )
    }
    if (content.steps) {
      contentMatch = content.steps.some(step => 
        step.title.toLowerCase().includes(query) ||
        step.description.toLowerCase().includes(query)
      )
    }
    
    if (titleMatch || subtitleMatch || contentMatch) {
      results.push({
        id: key,
        ...content
      })
    }
  })
  
