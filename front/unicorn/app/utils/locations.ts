export function findRegionByCountry(country: string): LocationRegion | null {
  const norm = (s: string) => s.trim().toLowerCase()
  const target = norm(country)
  return (
    locationRegions.find(r =>
      r.countries.some(c => norm(c) === target)
    ) || null
  )
}
